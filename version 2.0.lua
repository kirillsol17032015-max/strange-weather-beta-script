local function IMHP_fake_script() -- TextButton_3.LocalScript 
	local script = Instance.new('LocalScript', TextButton_3)

	local Players = game:GetService("Players")
	local RunService = game:GetService("RunService")
	
	local button = script.Parent
	local player = Players.LocalPlayer
	local noclip = false
	local connection = nil
	
	local function toggleNoclip()
		noclip = not noclip
	
		if noclip then
			button.Text = "NOCLIP: ON"
			button.BackgroundColor3 = Color3.fromRGB(0, 255, 0) -- Зеленый
	
			-- Запускаем цикл отключения коллизии
			connection = RunService.Stepped:Connect(function()
				local character = player.Character
				if character then
					for _, part in pairs(character:GetDescendants()) do
						if part:IsA("BasePart") and part.CanCollide then
							part.CanCollide = false
						end
					end
				end
			end)
		else
			button.Text = "NOCLIP: OFF"
			button.BackgroundColor3 = Color3.fromRGB(255, 0, 0) -- Красный
	
			-- Останавливаем цикл
			if connection then
				connection:Disconnect()
				connection = nil
			end
	
			-- Возвращаем коллизию основной части (опционально, она вернется сама)
			local character = player.Character
			if character and character:FindFirstChild("HumanoidRootPart") then
				character.HumanoidRootPart.CanCollide = true
			end
		end
	end
	
	button.MouseButton1Click:Connect(toggleNoclip)
	
end
