local function PJDGOP_fake_script() -- TextButton_4.LocalScript 
	local script = Instance.new('LocalScript', TextButton_4)

	local player = game.Players.LocalPlayer
	local mouse = player:GetMouse()
	local button = script.Parent
	
	local flying = false
	local speed = 100 -- Скорость полета
	local bv, bg -- Объекты физики
	
	function startFly()
		local character = player.Character
		if not character or not character:FindFirstChild("HumanoidRootPart") then return end
		local root = character.HumanoidRootPart
	
		flying = true
		button.Text = "FLY: ON"
		button.BackgroundColor3 = Color3.new(0, 1, 0)
	
		-- Создаем "толкатель"
		bv = Instance.new("BodyVelocity")
		bv.MaxForce = Vector3.new(1e6, 1e6, 1e6)
		bv.Velocity = Vector3.new(0, 0, 0)
		bv.Parent = root
	
		-- Создаем "вращатель" (чтобы персонаж не падал на бок)
		bg = Instance.new("BodyGyro")
		bg.MaxTorque = Vector3.new(1e6, 1e6, 1e6)
		bg.CFrame = root.CFrame
		bg.Parent = root
	
		-- Цикл полета
		spawn(function()
			while flying do
				game:GetService("RunService").RenderStepped:Wait()
				local camera = workspace.CurrentCamera
				-- Вычисляем направление полета относительно камеры
				local direction = Vector3.new(0,0,0)
	
				-- Простая логика: летим туда, куда смотрим, если зажат W
				local uis = game:GetService("UserInputService")
				if uis:IsKeyDown(Enum.KeyCode.W) then
					direction = direction + camera.CFrame.LookVector
				end
				if uis:IsKeyDown(Enum.KeyCode.S) then
					direction = direction - camera.CFrame.LookVector
				end
				if uis:IsKeyDown(Enum.KeyCode.A) then
					direction = direction - camera.CFrame.RightVector
				end
				if uis:IsKeyDown(Enum.KeyCode.D) then
					direction = direction + camera.CFrame.RightVector
				end
	
				bv.Velocity = direction * speed
				bg.CFrame = camera.CFrame
			end
		end)
	end
	
	function stopFly()
		flying = false
		button.Text = "FLY: OFF"
		button.BackgroundColor3 = Color3.new(1, 0, 0)
		if bv then bv:Destroy() end
		if bg then bg:Destroy() end
		if player.Character and player.Character:FindFirstChild("Humanoid") then
			player.Character.Humanoid.PlatformStand = false
		end
	end
	
	button.MouseButton1Click:Connect(function()
		if flying then
			stopFly()
		else
			startFly()
		end
	end)
	
end
