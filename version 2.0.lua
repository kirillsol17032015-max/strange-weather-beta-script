			-- Возвращаем всё как было
			for obj, oldVal in pairs(originalTransparencies) do
				if obj and obj.Parent then
					obj.Transparency = oldVal
				end
			end
			-- Очищаем таблицу
			originalTransparencies = {}
		end
	end
	
	button.MouseButton1Click:Connect(toggleTransparency)
	
