local function EOJFL_fake_script() -- TextButton_2.LocalScript 
	local script = Instance.new('LocalScript', TextButton_2)

	local button = script.Parent
	local players = game:GetService("Players")
	local runService = game:GetService("RunService")
	
	local localPlayer = players.LocalPlayer
	
	local enabled = false
	local espObjects = {}
	
	-- создание ESP
	local function createESP(player)
		if player == localPlayer then return end
	
		local function setup(character)
			if not enabled then return end
	
			local head = character:WaitForChild("Head", 5)
			local humanoid = character:WaitForChild("Humanoid", 5)
			local root = character:WaitForChild("HumanoidRootPart", 5)
	
			if not head or not humanoid or not root then return end
	
			-- 🔴 Highlight (видно через стены)
			local highlight = Instance.new("Highlight")
			highlight.FillColor = Color3.fromRGB(255, 0, 0)
			highlight.OutlineColor = Color3.fromRGB(255, 255, 255)
			highlight.FillTransparency = 0.5
			highlight.OutlineTransparency = 0
			highlight.DepthMode = Enum.HighlightDepthMode.AlwaysOnTop
			highlight.Parent = character
	
			-- BillboardGui
			local billboard = Instance.new("BillboardGui")
			billboard.Size = UDim2.new(0, 200, 0, 50)
			billboard.StudsOffset = Vector3.new(0, 2, 0)
			billboard.AlwaysOnTop = true
			billboard.Parent = head
	
			-- текст
			local label = Instance.new("TextLabel")
			label.Size = UDim2.new(1, 0, 1, 0)
			label.BackgroundTransparency = 1
			label.TextColor3 = Color3.new(1, 1, 1)
			label.TextStrokeTransparency = 0
			label.TextScaled = true
			label.Parent = billboard
	
			espObjects[player] = {
				gui = billboard,
				label = label,
				humanoid = humanoid,
				character = character,
				root = root,
				highlight = highlight
			}
		end
	
		if player.Character then
			setup(player.Character)
		end
	
		player.CharacterAdded:Connect(setup)
	end
	
	-- обновление текста
	runService.RenderStepped:Connect(function()
		if not enabled then return end
	
		local myChar = localPlayer.Character
		if not myChar or not myChar:FindFirstChild("HumanoidRootPart") then return end
	
		local myPos = myChar.HumanoidRootPart.Position
	
		for player, data in pairs(espObjects) do
			if data.character and data.root then
				local distance = (data.root.Position - myPos).Magnitude
				local hp = math.floor(data.humanoid.Health)
	
				data.label.Text = player.Name ..
					"\nHP: " .. hp ..
					"\nDist: " .. math.floor(distance) .. "m"
			end
		end
	end)
	
	-- очистка ESP
	local function clearESP()
		for _, data in pairs(espObjects) do
			if data.gui then
				data.gui:Destroy()
			end
			if data.highlight then
				data.highlight:Destroy()
			end
		end
		espObjects = {}
	end
	
	-- нажатие кнопки
	button.MouseButton1Click:Connect(function()
		enabled = not enabled
	
		if enabled then
			-- включено
			button.BackgroundColor3 = Color3.fromRGB(255, 255, 255)
	
			for _, player in pairs(players:GetPlayers()) do
				createESP(player)
			end
	
		else
			-- выключено
			button.BackgroundColor3 = Color3.fromRGB(152, 152, 152)
			clearESP()
		end
	end)
	
	-- новые игроки
	players.PlayerAdded:Connect(function(player)
		if enabled then
			createESP(player)
		end
	end)
end
